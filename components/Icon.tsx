
import React from 'react';
import { Suit } from '../types';

interface IconProps {
  suit: Suit;
  className?: string;
}

export const SuitIcon: React.FC<IconProps> = ({ suit, className }) => {
  const color = (suit === Suit.Heart || suit === Suit.Diamond) ? 'text-red-600' : 'text-black';

  switch (suit) {
    case Suit.Spade:
      return <span className={`${color} ${className}`}>♠</span>;
    case Suit.Club:
      return <span className={`${color} ${className}`}>♣</span>;
    case Suit.Heart:
      return <span className={`${color} ${className}`}>♥</span>;
    case Suit.Diamond:
      return <span className={`${color} ${className}`}>♦</span>;
    default:
      return null;
  }
};

